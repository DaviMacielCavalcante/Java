package exercicios_instrucoes_de_controle_1;

public class SaidaNoFormatoTabela {

	public static void main(String[] args) {
		System.out.println("N" + "    " + "10*N" + " " + "100*N" + " " + "1000*N");
		for (int n = 1; n <=5; n++) {			
			System.out.println(n + "    " + 10*n + "   " + 100*n + "   " + 1000*n);
		}

	}

}
